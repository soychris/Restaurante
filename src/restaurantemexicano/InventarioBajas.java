    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        
        if(!txtArticulo.getText().isEmpty())
        {
            Pool metodosPool = new Pool();
            java.sql.Connection cn = null;
            
            String strCodigo = txtCodigo.getText();
            String queryElimina = "DELETE FROM inventario WHERE codigo LIKE '" + strCodigo + "'";
            
            
            try{
                cn=metodosPool.dataSource.getConnection();
               
                if(cn!=null){
                    System.out.println(queryElimina);
                    cn.prepareStatement(queryElimina).execute();
                    JOptionPane.showMessageDialog(null, "Registro eliminado, favor de verificar.");
                    LimpiaFormulario();
                }
                
            }catch(Exception e){
                
            }finally{

                try {

                    cn.close();

                } catch (SQLException ex) {

                    System.out.println(ex);
                }
            }
            
        }else
        {
            JOptionPane.showMessageDialog(null, "No ha seleccionado un registro, favor de verificar.");
        }
        
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        Pool metodosPool = new Pool();
        java.sql.Connection cn = null;
        
        String codigo = txtCodigo.getText();
        
        String queryBuscar = "SELECT * FROM Inventario Where codigo Like '" + codigo + "'";
        System.out.println(queryBuscar);
        
        try{
            cn=metodosPool.dataSource.getConnection();
            java.sql.Statement st = cn.createStatement();
            
            if(cn!=null){
                
                ResultSet rs = st.executeQuery(queryBuscar);
                if(rs.next())
                {
                    txtArticulo.setText(rs.getString("articulo"));
                    txtSucursal.setText(rs.getString("sucursal"));
                    txtExistencia.setText(rs.getString("existencia"));
                    txtMarca.setText(rs.getString("marca"));
                    
                    
                }else
                {
                    JOptionPane.showMessageDialog(null, "No se encontr贸 registro, favor de verificar.");
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

