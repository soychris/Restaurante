    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
            Pool metodospool = new Pool();
            java.sql.Connection cn = null;
            
        String strCodigo = txtCodigo.getText();
        String strArticulo = txtArticulo.getText();
        String strSucursal = (String)cboSucursales.getSelectedItem();
        int intSucursal = cboSucursales.getSelectedIndex();
        String strExistencia = txtExistencia.getText();
        int intMarca = cboMarca.getSelectedIndex();
        String strMarca = (String)cboMarca.getSelectedItem();
        
        String queryInsert = "INSERT INTO inventario "
                + "(codigo, articulo, sucursal, existencia, marca, idMarca, idSucursal) "
                + "VALUES (" + strCodigo + ",'" + strArticulo + "','" + strSucursal+ "'," + strExistencia + ",'" + strMarca + "'," + intSucursal + "," + intMarca + ")";
        
        System.out.println(queryInsert);


        try {

            cn=metodospool.dataSource.getConnection();
            
            
            if(cn!=null){
                
                cn.prepareStatement(queryInsert).execute();
                                
                JOptionPane.showMessageDialog(null, "Registro exitoso.");
                
                LimpiaCampos();

            }

        } catch (SQLException e) {

            System.out.println(e);

        }finally{


                try {

                    cn.close();
                   
                }catch (SQLException ex) {

                    System.out.println(ex);

                }


        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

