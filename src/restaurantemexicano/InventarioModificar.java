    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        Pool metodosPool = new Pool();
        java.sql.Connection cn = null;
        
        String strCodigo = txtCodigo.getText();
        String queryBuscar = "SELECT * FROM inventario WHERE codigo LIKE '" + strCodigo + "'";
        
        System.out.println(queryBuscar);
        
        try{
            cn = metodosPool.dataSource.getConnection();
            java.sql.Statement st = cn.createStatement();
            if(cn!=null){

                ResultSet rs = st.executeQuery(queryBuscar);

                if(rs.next())
                {
                    txtArticulo.setText(rs.getString("articulo"));
                    txtExistencia.setText(rs.getString("existencia"));
                    cboSucursal.setSelectedIndex(rs.getInt("idSucursal"));
                    cboMarca.setSelectedIndex(rs.getInt("idMarca"));
                    txtCodigo.setEnabled(false);
                    
                    
                }else
                {
                    JOptionPane.showMessageDialog(null, "No se encontr贸 registro, favor de verificar.");
                }
            }
            
        }catch(Exception e){
            
        }finally{

                try {

                    cn.close();

                } catch (SQLException ex) {

                    System.out.println(ex);

                }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

