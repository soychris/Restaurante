    public frmConsultas() {
        initComponents();
        
    Pool metodospool = new Pool();    

    //Creamos objeto tipo Connection    
        
    PreparedStatement pst = null;
    ResultSet result = null;

    //Creamos la Consulta SQL
    String SQL = "SELECT * FROM nomina_personal";
    
    try{
       //Establecemos conexi칩n con la BD 
       conectar = metodospool.dataSource.getConnection();  
       //Preparamos la consulta SQL
       pst = conectar.prepareStatement(SQL);
       //Ejecutamos la consulta
       result = pst.executeQuery();        
       
       DefaultTableModel modelo = new DefaultTableModel();
       jtNomina.setModel(modelo);
       
       
       ResultSetMetaData resultSetMetaData = result.getMetaData();
       int cantidadColumnas = resultSetMetaData.getColumnCount();
        
       // Creamos las columnas.
        modelo.addColumn("id_nomina_personal");
        modelo.addColumn("nombre");
        modelo.addColumn("salario_base");
        modelo.addColumn("desempenio");
        modelo.addColumn("edad");
        modelo.addColumn("puesto");
        modelo.addColumn("area");
        modelo.addColumn("sueldo");
        modelo.addColumn("incentivo");
        
        
        // Bucle para cada resultado en la consulta
        while (result.next())
        {
           // Se crea un array que ser치 una de las filas de la tabla.
           Object [] fila = new Object[cantidadColumnas]; // Hay tres columnas en la tabla

           // Se rellena cada posici칩n del array con una de las columnas de la tabla en base de datos.
           for (int i=0; i<cantidadColumnas; i++)
              fila[i] = result.getObject(i+1); // El primer indice en rs es el 1, no el cero, por eso se suma 1.

           // Se a침ade al modelo la fila completa.
           modelo.addRow(fila);
        }
       
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e); 
    }
    
        
        
    }

